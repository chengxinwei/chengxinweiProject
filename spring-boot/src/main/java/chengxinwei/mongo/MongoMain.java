package chengxinwei.mongo;

import chengxinwei.mongo.listener.ApplicationStartupListener;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.EnableAutoConfiguration;
import org.springframework.context.annotation.ImportResource;
import org.springframework.stereotype.Controller;

/**
 * Created by xinwei.cheng on 2015/7/1.
 */
@Controller
@EnableAutoConfiguration
@ImportResource("/spring-mongo.xml")
public class MongoMain {

    public static void main(String[] args) throws Exception {
        SpringApplication springApplication = new SpringApplication(MongoMain.class);
        springApplication.addListeners(new ApplicationStartupListener());
        springApplication.run(args);
    }




}
